export const offerDesignTool = {
  name: "engagement_design",
  async invoke(args: any) {
    const { clientChallenge, scope, timelineWeeks, apiKeys } = args;

    const userPrompt = `
Client Challenge: ${clientChallenge}
Proposed Scope: ${scope}
Timeline: ${timelineWeeks} weeks

Design a complete consulting engagement including:
1. Value proposition (quantified)
2. Scope of work (in/out, milestones)
3. Pricing options (value-based, not time-based)
4. Success metrics
5. Contract terms and conditions
6. Expansion opportunities

Output as JSON with complete proposal structure.
    `.trim();

    try {
      const raw = await llmClient.call({
        systemPrompt: OFFER_SYSTEM_PROMPT,
        userPrompt,
        agentName: "agent5_offer",
        apiKeys,
      });

      const jsonMatch = raw.match(/\{[\s\S]*\}/);
      const jsonText = jsonMatch ? jsonMatch[0] : raw;
      return JSON.parse(jsonText);
    } catch (error) {
      console.error("Agent 5 error:", error);
      throw error;
    }
  },
};
